let a = 10;
let b = 3;

// Addition (+)
console.log("10 + 3 =", a + b);  // Output: 13

// Subtraction (-)
console.log("10 - 3 =", a - b);  // Output: 7

// Multiplication (*)
console.log("10 * 3 =", a * b);  // Output: 30

// Division (/)
console.log("10 / 3 =", a / b);  // Output: 3.3333333333333335

// Exponentiation (**)
console.log("10 ** 3 =", a ** b);  // Output: 1000

// Modulus (%)
console.log("10 % 3 =", a % b);  // Output: 1
// 10 % 3
// 9 / 3 = 0
// 10 - 9 = 1