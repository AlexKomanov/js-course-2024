let x = 10;

// Simple assignment (=)
console.log("x = 10:", x);

// Addition assignment (+=)
x += 5;
console.log("x += 5:", x);  // Output: 15

// Subtraction assignment (-=)
x -= 3;
console.log("x -= 3:", x);  // Output: 12

// Multiplication assignment (*=)
x *= 2;
console.log("x *= 2:", x);  // Output: 24

// Division assignment (/=)
x /= 4;
console.log("x /= 4:", x);  // Output: 6

// Modulus assignment (%=)
x %= 4;
console.log("x %= 4:", x);  // Output: 2  --> 6 % 4   -> 6 - 4 * 1 = 2

// Exponentiation assignment (**=)
x **= 3;
console.log("x **= 3:", x);  // Output: 8

// Increment (++)
console.log("++x:", ++x);  // Prefix increment, Output: 9
console.log("x++:", x++);  // Postfix increment, Output: 9
console.log("After x++:", x);  // Output: 10

// Decrement (--)
console.log("--x:", --x);  // Prefix decrement, Output: 9
console.log("x--:", x--);  // Postfix decrement, Output: 9
console.log("After x--:", x);  // Output: 8

